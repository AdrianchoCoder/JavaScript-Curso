// Concatenacion de Cadenas en Js
let cadena1 = 'Hola';
let cadena2 = 'Mundo';
let cadena3 = cadena1 + " " + cadena2; // De esta manera tan sencilla podemos concatenar dos cadenas de texto o mas...
// Tambien lo podemos hacer de la siguiente manera con string interpolation:
let cadena4 = `${cadena1} ${cadena2}`; // con ese espacio que dejamos entre cadena1 y cadena2 tambien se puede realizar.
console.log(cadena3);
console.log(cadena4);